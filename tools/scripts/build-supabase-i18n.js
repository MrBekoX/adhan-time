/**
 * Generates the shared push notification i18n artifact for the Supabase
 * edge function from the mobile locale catalogs.
 *
 * Outputs:
 *   - supabase/functions/_shared/i18n.json   — source-of-truth JSON, used
 *     by the parity test so a translation drift between mobile and server
 *     triggers a CI failure.
 *   - supabase/functions/_shared/i18n.gen.ts — runtime artifact with the
 *     catalog inlined as a `const`. Imported by push-prayer in the Deno
 *     edge runtime and by the i18n unit tests in Jest+Node, so the same
 *     module works in both environments (no `import.meta`, no JSON-import
 *     attributes).
 *
 * Run after editing any prayer.* string in `locales/*.json`:
 *   npm run build:supabase-i18n
 *
 * Plain Node CommonJS (no TypeScript) so the script can run without
 * introducing a ts-node devDependency for CI.
 */
'use strict';

const fs = require('fs');
const path = require('path');

const REPO_ROOT = path.resolve(__dirname, '..', '..');
const LOCALES = ['tr', 'en', 'ar', 'zh'];
const SHARED_DIR = path.join(REPO_ROOT, 'supabase', 'functions', '_shared');
const JSON_OUT = path.join(SHARED_DIR, 'i18n.json');
const TS_OUT = path.join(SHARED_DIR, 'i18n.gen.ts');

function readMobileLocale(locale) {
  const file = path.join(REPO_ROOT, 'locales', `${locale}.json`);
  return JSON.parse(fs.readFileSync(file, 'utf8'));
}

function buildShared() {
  const out = {};
  for (const locale of LOCALES) {
    const mobile = readMobileLocale(locale);
    if (!mobile.prayer) {
      throw new Error(`mobile locale ${locale}.json missing "prayer" namespace`);
    }
    out[locale] = { prayer: mobile.prayer };
  }
  return out;
}

function renderTsModule(shared) {
  const json = JSON.stringify(shared, null, 2);
  return [
    '// AUTO-GENERATED by tools/scripts/build-supabase-i18n.js — DO NOT EDIT.',
    '// Regenerate with `npm run build:supabase-i18n` after editing locales/*.json.',
    '',
    'export const SHARED_I18N = ' + json + ' as const;',
    '',
  ].join('\n');
}

function main() {
  const shared = buildShared();
  fs.mkdirSync(SHARED_DIR, { recursive: true });
  fs.writeFileSync(JSON_OUT, JSON.stringify(shared, null, 2) + '\n', 'utf8');
  fs.writeFileSync(TS_OUT, renderTsModule(shared), 'utf8');
  console.log(`wrote ${JSON_OUT}`);
  console.log(`wrote ${TS_OUT}`);
}

if (require.main === module) {
  main();
}

module.exports = { buildShared };
